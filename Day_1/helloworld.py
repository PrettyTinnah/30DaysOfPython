#arithmetic operations in python

print(2 + 3)   # addition(+)
print(3 - 1)   # subtraction(-)
print(2 * 3)   # multiplication(*)
print(3 / 2)   # division(/)
print(3 ** 2)  # exponential(**)
print(3 % 2)   # modulus(%)
print(3 // 2)  # Floor division operator(//)

# Checking data types

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(1 + 3j))              # Complex //conatains numbers which one is the real part of the number and the other is imaginary 
print(type('Ci-cada'))          # String
print(type([1, 2, 3]))           # List  //defined as data types that are used to store multiple items in a single variable
print(type({'name':'Ci-cada'})) # Dictionary //defined as data types that store values in key(name):value(Ci-cada) pairs and it is ordered
print(type({9.8, 3.14, 2.7}))    # Set //similar to strings but are unordered unchangeable and unindexed as opposed to lists
print(type((9.8, 3.14, 2.7)))    # Tuple // same to sets they are unchengeable and used to represent fixed collection of items
